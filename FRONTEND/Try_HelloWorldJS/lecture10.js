// ++ 연산자로 바꾸기

var b = 1;
console.log(b); // 1
console.log(++b); // 2 ▶️ ++ 연산자로 수정
console.log(b); // 2

console.log("============");

var c = 1;
console.log(c); // 1
console.log(--c); // 0 ▶️ -- 연산자로 수정
console.log(c); // 0

console.log("============");

var d = 1;
console.log(d); // 1
console.log(d++); // 1  ++ 연산자를 변수 뒤에 썼을 때
console.log(d); // 2

